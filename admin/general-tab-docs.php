<!-- Auth Token -->
<div id="twilio_right_docs_title_token" style="display: none;">
    Twilio Auth Token
</div>
<div id="twilio_right_docs_content_token" style="display: none;">
    When you create a new account or subaccount, Twilio generates an Account SID and Auth token for that account. You
    can find these API credentials on the dashboard page of your account.
    <br><br>
    These credentials are frequently used to communicate with Twilio via the CLI, SDK's, or using the API directly.
    There is only one auth token by default so you should avoid sharing this auth token to minimize the risk of it
    becoming compromised.
    <br><br>
    Click the following link for more information:
    <ul>
        <li>
            - <a href="https://www.twilio.com/blog/better-twilio-authentication-csharp-twilio-api-keys" target="_blank">Better
                Twilio Authentication with Twilio API Keys</a>
        </li>
    </ul>
</div>
