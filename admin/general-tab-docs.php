    Twilio Account SID
</div>
<div id="twilio_right_docs_content_sid" style="display: none;">
    A String Identifier (SID) is a unique key that is used to identify specific resources.
    <br><br>
    Twilio generates an Account String Identifier (SID) and an Auth token when you create a Twilio account. This key is
    for all intents and purposes a master key that can be used to perform any function available in the Twilio API.
